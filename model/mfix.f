PROGRAM MFIX

  !-----------------------------------------------
  ! Modules
  !-----------------------------------------------
  USE leqsol, only: SOLVER_STATISTICS, report_solver_stats
  USE main, only: setup, start, step, end, mfix_waiting, request_pending, nit_total, finish
  USE run, only: nstep, auto_restart, automatic_restart, iter_restart, time, tstop

  IMPLICIT NONE

  CALL SETUP

  ! AUTO RESTART LOOP
  DO

     CALL START

     ! The TIME loop begins here.............................................
     DO

        ! wait for request to finish
        do while(1.eq.request_pending)
           mfix_waiting = 1
           ! without a write, this loop gets optimized away
           write(*,fmt='(a)',advance='no') ''
        enddo
        mfix_waiting = 0

        CALL STEP

        IF (FINISH) EXIT
        IF (TIME >= TSTOP) EXIT

     ! The TIME loop ends here....................................................
     ENDDO

     IF(SOLVER_STATISTICS) CALL REPORT_SOLVER_STATS(NIT_TOTAL, NSTEP)

     IF(AUTO_RESTART.AND.AUTOMATIC_RESTART.AND.ITER_RESTART.LE.10) CYCLE
     EXIT
  ENDDO

  CALL END

  STOP

END PROGRAM MFIX
